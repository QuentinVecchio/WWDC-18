let PI = CGFloat.pi

func shortestAngleBetween(angle1: CGFloat, angle2: CGFloat) -> CGFloat {
    let TWO_PI = PI * 2.0
    var angle = (angle2 - angle1).truncatingRemainder(dividingBy: TWO_PI)
    if angle >= PI {
        angle = angle - TWO_PI
    }
    if angle <= -PI {
        angle = angle + TWO_PI
    }
    return angle
}
